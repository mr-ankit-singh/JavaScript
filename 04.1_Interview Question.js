// What is difference between == and === ?
var num1 = 5
var num2 ='5'
console.log(typeof(num1))
console.log(typeof(num2))
// == : it only check the value
console.log(num1 == num2)

var num1 =5
var num2 ='5'
console.log(typeof(num1))
console.log(typeof(num2))
// === : it check the value as well as datatype
console.log(num1 === num2)