/*
#__ DataTypes in JavaScript __#

   + There are two types of  datatypes in javascript
    
    + Primitive Data
    -A primitive data value is a single simple data value with no additional properties and methods.
    -The typeof operator can return one of these primitive types:
    * There are 6 types of primitive datatypes
      - string
      - number
      - boolean
      - undefined
      - bigint
      - symbol

    + Complex Data
    -The typeof operator can return one of two complex types:
    * Complex/Reference datatypes
      -function
      -object
      
    Note: The typeof operator returns "object" for objects, arrays, and null.
    Note: The typeof operator does not return "object" for functions.

   + Type conversion

      - String()  - Convert to string
      - Number()  - Convert to Number
      - Boolean() - Convert to Boolean
      
*/